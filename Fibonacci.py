
def fib(n):
    a = 0
    b = 1

    if n < 0:                   #input negative
        print("Invalid Number")
    elif():
        print(a)
        print(b)
    for i in range(2, n):       #2nd pos fill
        c = a + b
        a = b
        b = c
        print(c)

